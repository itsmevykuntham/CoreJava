package com.java.basics;

public class Comments {
    public static void main(String[] args) {
        System.out.println("// this is a single line comment");
        System.out.println("/* this " +
                "\nis " +
                "\nmulti line comment " +
                "\n*/");
        System.out.println("/*" +
                "\n*documentation" +
                "\n*comment " +
                "\n*/");
    }
}
